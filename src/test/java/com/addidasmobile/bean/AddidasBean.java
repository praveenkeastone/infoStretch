package com.addidasmobile.bean;

public class AddidasBean {
	
	private  String productName;
	private  String productPrice;
	

	public void setProductName(String addproductName) {
		this.productName = addproductName;
	}
	public String getProductName() {
		return productName;
	}
	
	public void setProductPrice(String addproductPrice) {
		this.productPrice= addproductPrice;
	}
	
	public String getProductPrice() {
		return productPrice;
	}
	
}
